package com.soulous.ai;

/**
 * 【AI 审核结果的状态枚举。
 *
 * <p>当用户提交任务完成证据后，AI 会对提交内容进行审核，
 * 根据审核结论返回以下四种状态之一，驱动后续业务流程。</p>
 */
public enum AiReviewResult {

    /** 【通过——AI 认为提交内容满足任务要求，任务标记为完成】 */
    PASS,

    /** 【驳回——AI 认为提交内容不满足要求，需用户重新提交】 */
    REJECT,

    /** 【需补充——AI 认为提交内容基本合格但缺少关键信息，要求用户补充】 */
    NEED_MORE,

    /** 【转人工——AI 无法自动判断，转交人工审核】 */
    MANUAL
}
