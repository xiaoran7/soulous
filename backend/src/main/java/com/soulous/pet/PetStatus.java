package com.soulous.pet;

/**
 * 【宠物状态枚举：描述宠物当前的情绪和活动状态。
 *  状态由各种事件驱动变化（任务开始、完成、驳回等），
 *  前端可根据不同状态展示不同的宠物动画和表情。】
 */
public enum PetStatus {
    /** 【正常状态：宠物空闲时的默认状态】 */
    NORMAL,
    /** 【工作中：用户正在执行学习任务时的状态】 */
    WORKING,
    /** 【复核中：任务已提交等待复核的状态】 */
    REVIEWING,
    /** 【开心：获得经验奖励后的积极状态】 */
    HAPPY,
    /** 【兴奋：升级时触发的高亢状态】 */
    EXCITED,
    /** 【困倦：需要补充材料或长时间等待时的疲惫状态】 */
    SLEEPY,
    /** 【伤心：任务被驳回时的消极状态】 */
    SAD,
    /** 【自豪：高质量完成任务（经验达到基础经验80%以上）时的状态】 */
    PROUD
}
