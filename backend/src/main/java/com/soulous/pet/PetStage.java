package com.soulous.pet;

/**
 * 【宠物成长阶段枚举：描述宠物的生命周期阶段，由等级决定。
 *  不同阶段对应不同的宠物外观，前端根据阶段展示对应的 sprite。】
 */
public enum PetStage {
    /** 【蛋阶段：等级1，宠物初始状态，尚未孵化】 */
    EGG,
    /** 【幼年阶段：等级2-3，宠物刚孵化，外观幼小可爱】 */
    CHILD,
    /** 【成长中阶段：等级4-7，宠物逐渐长大，外观变化明显】 */
    GROWING,
    /** 【成年阶段：等级8及以上，宠物完全成熟，外观最终形态】 */
    ADULT
}
