--   非破坏式：保留旧 pet 表不动，新建 owned_pet 并把现有宠物迁移过去（active=true，默认品种 feixue）。
--   每只宠物独立保有等级/经验/心情，互不共享；active=true 为出战宠物。结构与 mysql 版本一致。】
-- Pet market: species catalog + per-user owned pets (non-destructive; old pet table left intact).

CREATE TABLE pet_species (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,  -- 【中文：主键】
    slug VARCHAR(64) NOT NULL UNIQUE,                         -- 【中文：唯一标识】
    name VARCHAR(64) NOT NULL,                                -- 【中文：展示名】
    rarity VARCHAR(32),                                       -- 【中文：稀有度 COMMON/RARE/EPIC】
    price INT NOT NULL,                                       -- 【中文：金币价格，0=免费】
    starter BOOLEAN NOT NULL,                                 -- 【中文：是否入门款（可免费领养首只）】
    sprite_path VARCHAR(255),                                 -- 【中文：前端 spritesheet 路径】
    description VARCHAR(255),                                 -- 【中文：简介】
    sort_order INT NOT NULL                                   -- 【中文：展示排序】
);

