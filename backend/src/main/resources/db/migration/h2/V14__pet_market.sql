CREATE TABLE owned_pet (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,   -- 【中文：主键】
    user_id BIGINT NOT NULL,                                  -- 【中文：所属用户】
    species_id BIGINT,                                        -- 【中文：品种】
    name VARCHAR(255),                                        -- 【中文：宠物名】
    avatar_url VARCHAR(255),                                  -- 【中文：自定义头像】
    level INT,                                                -- 【中文：等级】
    current_exp INT,                                          -- 【中文：当前经验】
    next_level_exp INT,                                       -- 【中文：升级所需经验】
    mood INT,                                                 -- 【中文：心情 0-100】
    satiety INT,                                              -- 【中文：饱腹感 0-100】
    growth_stage VARCHAR(32),                                 -- 【中文：成长阶段】
    status VARCHAR(32),                                       -- 【中文：状态】
    active BOOLEAN NOT NULL DEFAULT TRUE,                     -- 【中文：是否出战】
    acquired_at TIMESTAMP(6),                                 -- 【中文：获得时间】
    created_at TIMESTAMP(6),                                  -- 【中文：创建时间】
    updated_at TIMESTAMP(6),                                  -- 【中文：更新时间】
    CONSTRAINT fk_owned_pet_user FOREIGN KEY (user_id) REFERENCES user_account(id),
    CONSTRAINT fk_owned_pet_species FOREIGN KEY (species_id) REFERENCES pet_species(id)
);
CREATE INDEX idx_owned_pet_user ON owned_pet(user_id);
CREATE INDEX idx_owned_pet_user_active ON owned_pet(user_id, active);

